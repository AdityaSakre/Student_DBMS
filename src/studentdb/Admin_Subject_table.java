    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        boolean valid = validatesub();
        if(valid){
            DBconn dbase = new DBconn();
            BasicDBObject obj = new BasicDBObject();
            obj.put("SubjectId",jTxt_subid.getText());
            obj.put("SubjectName",jTxt_subname.getText());
            obj.put("FacultyId",jTxt_facid.getText());
            String classs = jComboBox1.getSelectedItem().toString();
            obj.put("Class",classs);
            String semester = jComboBox2.getSelectedItem().toString();
            obj.put("Semester",semester);
            String  internal = jComboBox3.getSelectedItem().toString();
            obj.put("Internal",internal);
            String  Practical = jComboBox3.getSelectedItem().toString();
            obj.put("Practical",Practical);
            dbase.connect("DB","SubjectStructure",obj);
            
            JOptionPane.showMessageDialog(rootPane,"Succesfully Submitted");
        }
        else{
        }
         
    }//GEN-LAST:event_jButton1ActionPerformed

