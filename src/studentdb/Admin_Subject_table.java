    private void jTxt_subidFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTxt_subidFocusLost
        // TODO add your handling code here:
        String subid = jTxt_subid.getText();
        if(subid.matches("[0-9]{5}") || subid.isEmpty()){
            if(!subid.isEmpty())  {
                jLabV_subid.setIcon(icojk);
                jLabV_subid.setText("Okay");
                jLab_subid.setForeground(null);
                jLab_subid.setText(null);
            }
            else{
                 
                jLabV_subid.setIcon(null);
                jLabV_subid.setText(null);
                jLab_subid.setForeground(null);
                jLab_subid.setText(null);
            }
        }
        else{ 
            jLabV_subid.setText("Invalid");
            jLabV_subid.setIcon(icox);
            jLab_subid.setForeground(Color.red);
            jLab_subid.setText("Should be contain 5 digits only");
           
        }
    }//GEN-LAST:event_jTxt_subidFocusLost

