    boolean validatesub(){
        boolean valid = true;
        String subid = jTxt_subid.getText();
        if(subid.matches("[0-9]{5}")){
            jLabV_subid.setIcon(icojk);
            jLabV_subid.setText("Okay");
            jLab_subid.setForeground(null);
            jLab_subid.setText(null);
        }
        else{ 
            jLabV_subid.setText("Invalid");
            jLabV_subid.setIcon(icox);
            jLab_subid.setForeground(Color.red);
            jLab_subid.setText("Should be contain 5 digits only");
            valid =false;
        }
        String Facid = jTxt_facid.getText();
        if(Facid.matches("[0-9]{5}[A-Z]")){
            jLabV_facid.setIcon(icojk);
            jLabV_facid.setText("Okay");
            jLab_facid.setForeground(null);
            jLab_facid.setText(null);
        }
        else{ 
            jLabV_facid.setText("Invalid");
            jLabV_facid.setIcon(icox);
            jLab_facid.setForeground(Color.red);
            jLab_facid.setText("Should be contain 5 digits and a character");
            valid = true;
        }
        return valid;
        
    }
            
    
